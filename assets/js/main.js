// Error message elements
var nameError = document.getElementById("name-error");
var placeError = document.getElementById("place-error");
var phoneError = document.getElementById("phone-error");
var emailError = document.getElementById("email-error");
var messageError = document.getElementById("message-error");

// Validate Name
function validateName() {
  var name = document.getElementById('contact-name').value.trim();

  if (name.length === 0) {
      nameError.innerHTML = "Name is required";
      return false;
  } else if (!name.match(/^[A-Za-z\s]+$/)) {
      nameError.innerHTML = "Name must contain only letters and spaces";
      return false;
  }

  nameError.innerHTML = ""; // Clear the error if the input is valid
  return true;
}


// Validate Place
function validatePlace() {
  var place = document.getElementById('contact-place').value.trim();

  if (place.length === 0) {
      placeError.innerHTML = "Place is required";
      return false;
  } else if (!place.match(/^[A-Za-z\s]+$/)) {
    placeError.innerHTML = "Place must contain only letters and spaces";
    return false;
}


  placeError.innerHTML = "";
  return true;
}


// Validate Email
function validateMail() {
    var mail = document.getElementById('email-field').value.trim();

    if (mail.length === 0) {
        emailError.innerHTML = "Email is required";
        return false;
    } else if (!mail.match(/^\S+@\S+\.\S+$/)) {
        emailError.innerHTML = "Enter a valid email address";
        return false;
    }

    emailError.innerHTML = "";
    return true;
}

// Validate Phone number
function validatePhone() {
  var phone = document.getElementById('phone-field').value.trim();

  if (phone.length === 0) {
      phoneError.innerHTML = "Phone number is required";
      return false;
  } else if (!phone.match(/^\d{10}$/)) {
    phoneError.innerHTML = "Phone number must be exactly 10 digits";
    return false;
  }

  phoneError.innerHTML = "";
  return true;
}

// Validate Message
function validateMessage() {
    var message = document.getElementById('message-field').value.trim();
    var required = 20; // Minimum message length
    var left = required - message.length;

    if (message.length === 0) {
        messageError.innerHTML = "Message is required";
        return false;
    } else if (left > 0) {
        messageError.innerHTML = left + " more characters required";
        return false;
    }

    messageError.innerHTML = "";
    return true;
}

// Validate Form Before Submission
function validateForm() {
    var isValidName = validateName();
    var isValidPlace = validatePlace();
    var isValidPhone = validatePhone();
    var isValidMail = validateMail();
    var isValidMessage = validateMessage();

    return isValidName && isValidPlace && isValidPhone && isValidMail && isValidMessage;
}
